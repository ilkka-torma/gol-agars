# This computes HatCA.
def forced_preimage(pat, iterations = 1):
    domain = set(pat)
    pre_domain = set(nbr for vec in domain for nbr in neighborhood(vec))
    maybe_forced = set(pre_domain)
    
    clauses, variables = gol_preimage(pat, iterations = iterations)
    with Glucose4(bootstrap_with=clauses) as solver:
        i = 0
        while solver.solve():
            #print(len(maybe_forced), "vs", len(domain), len(pre_domain))
            i += 1
            if i == 1:
                # Get initial preimage
                pre = model_to_pattern(solver.get_model(), variables)
            else:
                if i%10 == 0:
                    print("\t\t\tRound {}, {} remain".format(i, len(maybe_forced)))
                new = model_to_pattern(solver.get_model(), variables)
                maybe_forced -= set(vec for vec in new if new[vec] != pre[vec])
                solver.add_clause([(-1 if pre[vec] else 1)*variables[vec]
                                   for vec in maybe_forced])

    return {vec:pre[vec] for vec in pre_domain if vec in maybe_forced}

"""
This computes the intersection of domains of HAT images of multiple patterns,
with the possibility to include a seed (not used now).
"""
def common_forced_part(pats, return_pat=False, seeds=None, iterations=1):
    # Assume pats have common domain
    domain = set(pats[0])
    pre_domain = set(nbr for vec in domain for nbr in neighborhood(vec, iterations))
    maybe_forced = set(pre_domain)
    k = 0
    for (ip,pat) in enumerate(pats):
        k += 1
        if len(pats)>1:
            print("Pattern", k)
        if seeds != None:
            clauses, variables = gol_preimage(pat, seeds[ip], iterations=iterations)
        else:
            clauses, variables = gol_preimage(pat, iterations=iterations)
        with Glucose4(bootstrap_with=clauses) as solver:
            i = 0
            while solver.solve():
                i += 1
                if i == 1:
                    # Get initial preimage
                    pre = model_to_pattern(solver.get_model(), variables)
                else:
                    if i%10 == 0 or i < 10:
                        print("Round {}, {} remain".format(i, len(maybe_forced)))
                    new = model_to_pattern(solver.get_model(), variables)
                    maybe_forced -= set(vec for vec in new if new[vec] != pre[vec])
                    solver.add_clause([(-1 if pre[vec] else 1)*variables[vec]
                                       for vec in maybe_forced])
        print("pattern done, now looks like")
        print_pattern({vec: (1 if vec in domain else '+') if vec in maybe_forced else (0 if vec in domain else '.')
                for vec in pre_domain})
        print()
    if return_pat:
        return {vec: (1 if vec in domain else '+') if vec in maybe_forced else (0 if vec in domain else '.')
                for vec in pre_domain}
    else:
        return maybe_forced

def common_forced_part_of_padded_shifts(mat, xpadding, ypadding, iterations):
    pats = []
    t = time.time()
    
    for x in range(len(mat[0])):
        for y in range(len(mat)):
                
            mat2 = vshift(hshift(mat, x), y)
            mat2 = pad_matrix(mat2, xpadding, ypadding)
            pat = {(i,j):mat2[j][i] for i in range(len(mat2[0])) for j in range(len(mat2))}

            pats.append(pat)

    fp = common_forced_part(pats, True, None, iterations=iterations)
    print_pattern(fp)

    print("time:", time.time() - t)

def color_relative_to_set(pat, origdom):
    tp = {}
    for vec in pat:
        if vec in origdom:
            tp[vec] = pat[vec]
        else:
            tp[vec] = 2+pat[vec]
    for vec in origdom:
        if vec not in pat:
            tp[vec] = 4
    return tp

